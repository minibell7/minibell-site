export default function FortuneClient() {
    const [isBroken, setIsBroken] = useState(false);
    const [message, setMessage] = useState<string | null>(null);

    const openCookie = () => {
        if (isBroken) {
            // Reset
            setIsBroken(false);
            setMessage(null);
        } else {
            // Break
            setIsBroken(true);
            const randomMsg = FORTUNES[Math.floor(Math.random() * FORTUNES.length)];
            setTimeout(() => {
                setMessage(randomMsg);
            }, 500);
        }
    };

    return (
        <div className={styles.container}>
            <div
                className={`${styles.cookieCard} glass-panel`}
                onClick={openCookie}
            >
                <div className={`${styles.cookie} ${isBroken ? styles.broken : ''}`}>
                    <span className={styles.emoji}>{isBroken ? 'ü•†' : 'ü•†'}</span>
                </div>

                <div className={`${styles.message} ${message ? styles.visible : ''}`}>
                    <p>"{message}"</p>
                </div>

                <p className={styles.instruction}>
                    {isBroken ? 'Click to try again' : 'Click the cookie to open!'}
                </p>
            </div>
        </div>
    );
}
